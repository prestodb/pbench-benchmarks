select  *
from (select avg(ss_list_price) B1_LP
            ,count(ss_list_price) B1_CNT
            ,count(distinct ss_list_price) B1_CNTD
      from store_sales
      where ss_quantity between 0 and 5
        and (ss_list_price between 35 and 35+10 
             or ss_coupon_amt between 595 and 595+1000
             or ss_wholesale_cost between 20 and 20+20)) B1,
     (select avg(ss_list_price) B2_LP
            ,count(ss_list_price) B2_CNT
            ,count(distinct ss_list_price) B2_CNTD
      from store_sales
      where ss_quantity between 6 and 10
        and (ss_list_price between 66 and 66+10
          or ss_coupon_amt between 13891 and 13891+1000
          or ss_wholesale_cost between 36 and 36+20)) B2,
     (select avg(ss_list_price) B3_LP
            ,count(ss_list_price) B3_CNT
            ,count(distinct ss_list_price) B3_CNTD
      from store_sales
      where ss_quantity between 11 and 15
        and (ss_list_price between 57 and 57+10
          or ss_coupon_amt between 9116 and 9116+1000
          or ss_wholesale_cost between 45 and 45+20)) B3,
     (select avg(ss_list_price) B4_LP
            ,count(ss_list_price) B4_CNT
            ,count(distinct ss_list_price) B4_CNTD
      from store_sales
      where ss_quantity between 16 and 20
        and (ss_list_price between 146 and 146+10
          or ss_coupon_amt between 2426 and 2426+1000
          or ss_wholesale_cost between 57 and 57+20)) B4,
     (select avg(ss_list_price) B5_LP
            ,count(ss_list_price) B5_CNT
            ,count(distinct ss_list_price) B5_CNTD
      from store_sales
      where ss_quantity between 21 and 25
        and (ss_list_price between 54 and 54+10
          or ss_coupon_amt between 14472 and 14472+1000
          or ss_wholesale_cost between 11 and 11+20)) B5,
     (select avg(ss_list_price) B6_LP
            ,count(ss_list_price) B6_CNT
            ,count(distinct ss_list_price) B6_CNTD
      from store_sales
      where ss_quantity between 26 and 30
        and (ss_list_price between 13 and 13+10
          or ss_coupon_amt between 2252 and 2252+1000
          or ss_wholesale_cost between 48 and 48+20)) B6
limit 100;
